# O programa mostra a soma,subtração, multiplicação e divisão de dois números dados pelo usuário

num1 = int(input("Informe o primeiro número: "))
num2 = int(input("Informe o segundo número: "))

print("\nSoma:", num1, "+", num2, "=", num1+num2)
print("Subtração:", num1, "-", num2, "=", num1-num2)
print("Multiplicação:", num1, "x", num2, "=", num1*num2)
print("Divisão:", num1, "/", num2, "=", num1/num2)
